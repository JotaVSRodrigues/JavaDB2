    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        String email = txtEmail.getText();
        String senha = new String(psdField.getPassword());
        
        AuthService loginService = new AuthService();
        
        try {
            boolean logado = loginService.loginUser(email, senha);
            if (logado) {
                JOptionPane.showMessageDialog(this, "Login realizado com sucesso");
            } else {
                // Manda o usuário para a tela de cadastro caso não houver email ou senha cadastrado
                JOptionPane.showMessageDialog(this, "Usuário ou senha inválidos");
                new TelaCadastro().setVisible(true);
                this.dispose();
            }
            
        } catch (SQLException e){
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Erro ao acessar o banco de dados");
        }
    }//GEN-LAST:event_btnLoginActionPerformed

