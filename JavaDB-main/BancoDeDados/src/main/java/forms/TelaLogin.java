    private void txtCadastro1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_txtCadastro1MouseClicked
        String email = JOptionPane.showInputDialog(this, "Digite seu email para alterar a senha: ");
        if (email == null || email.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Email obrigatorio");
        }
        if (!autenticacao.verificarEmail(email)) {
            JOptionPane.showMessageDialog(this, "Email não encontrado");
            return;
        }
        String novaSenha = JOptionPane.showInputDialog(this, "Digite seu email para alterar a senha: ");
        var service = new AuthService();
        service.atualizarSenha(email, novaSenha);
    }//GEN-LAST:event_txtCadastro1MouseClicked

